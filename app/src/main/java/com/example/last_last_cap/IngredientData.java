package com.example.last_last_cap;

public class IngredientData {
    private String name;
    private String date;

    public IngredientData(String name, String date) {
        this.name = name;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public String getDate() {
        return date;
    }
}
